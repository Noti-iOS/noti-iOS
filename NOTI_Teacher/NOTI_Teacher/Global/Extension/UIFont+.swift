import UIKit

extension UIFont {
    // Basic
    class func notoSansKR_Bold(size: CGFloat) -> UIFont{
        return UIFont(name: "NotoSansKR-Bold", size: size)!
    }
    
    class func notoSansKR_Medium(size: CGFloat) -> UIFont{
        return UIFont(name: "NotoSansKR-Medium", size: size)!
    }
    
    class func notoSansKR_Light(size: CGFloat) -> UIFont{
        return UIFont(name: "NotoSansKR-Light", size: size)!
    }
}
